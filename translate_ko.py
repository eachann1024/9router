# Detect Chinese characters
chinese_pattern = re.compile(r'[\u4e00-\u9fff]')

def translate_text(text):
    """Translate Chinese text to Korean"""
    if not chinese_pattern.search(text):
        return text
    
    # Try direct translation from map
    if text in translations:
        return translations[text]
    
    # For complex sentences, translate word by word
    result = text
    for zh_word, ko_word in sorted(translations.items(), key=lambda x: -len(x[0])):
        result = result.replace(zh_word, ko_word)
    
    return result

# Translate all values
translated_count = 0
for key, value in ko.items():
    if chinese_pattern.search(value):
        # Preserve ${variable} syntax
        parts = re.split(r'(\$\{[^}]+\})', value)
        translated_parts = []
        for part in parts:
            if part.startswith('${'):
                translated_parts.append(part)
            else:
                translated_parts.append(translate_text(part))
        
        new_value = ''.join(translated_parts)
        if new_value != value:
            ko[key] = new_value
            translated_count += 1
            print(f"✓ {key}")
            print(f"  {value} → {new_value}")

# Sort by key
ko_sorted = dict(sorted(ko.items()))

# Write back
with open('/Users/eachann/WorkMark/open-source/9router/public/i18n/literals/ko.json', 'w', encoding='utf-8') as f:
    json.dump(ko_sorted, f, ensure_ascii=False, indent=2)

print(f"\n✅ Translation complete!")
print(f"📊 Translated {translated_count} entries")
print(f"📝 Total entries: {len(ko_sorted)}")
